import * as React from "react";
import { VariantProps, cva } from "class-variance-authority";
import { cn } from "./utils";

const alertVariants = cva("relative flex justify-between items-top w-full rounded-lg border p-4", {
    variants: {
        variant: {
            default: "bg-background text-foreground",
            destructive:
                "text-destructive border-destructive/50 dark:border-destructive [&>svg]:text-destructive text-destructive",
        },
    },
    defaultVariants: {
        variant: "default",
    },
});

const Alert = React.forwardRef<
    HTMLDivElement,
    React.HTMLAttributes<HTMLDivElement> &
        VariantProps<typeof alertVariants> & { button?: React.ReactNode }
>(({ className, variant, button, ...props }, ref) => (
    <div ref={ref} role="alert" className={cn(alertVariants({ variant }), className)} {...props}>
        <div className="flex-1 flex items-center justify-between">
            {props.children}
            {button && <div className="ml-4">{button}</div>}
        </div>
    </div>
));
Alert.displayName = "Alert";

const AlertTitle = React.forwardRef<HTMLParagraphElement, React.HTMLAttributes<HTMLHeadingElement>>(
    ({ className, ...props }, ref) => (
        <h5
            ref={ref}
            className={cn("mb-1 font-medium leading-none tracking-tight", className)}
            {...props}
        />
    )
);
AlertTitle.displayName = "AlertTitle";

const AlertDescription = React.forwardRef<
    HTMLParagraphElement,
    React.HTMLAttributes<HTMLParagraphElement>
>(({ className, ...props }, ref) => (
    <div ref={ref} className={cn("text-sm [&_p]:leading-relaxed", className)} {...props} />
));
AlertDescription.displayName = "AlertDescription";

export { Alert, AlertTitle, AlertDescription };
